
// escape notation
//   \'\' for quote
//   \\  for back slash
//  \t for tab
// \n for new line
var tree = 'this is a "redwood" tree';
console.log(tree);

var pen = "this is a 'fountain' pen";
console.log(pen);

var food = 'this is a \'mango\' '
console.log(food);

var goods = 'this is a \n black power'
console.log(goods);

var tab = 'this is \t laptop'
console.log(tab);